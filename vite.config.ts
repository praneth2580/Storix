import { defineConfig, loadEnv } from 'vite'
import react from '@vitejs/plugin-react'
import { VitePWA } from 'vite-plugin-pwa'

// https://vitejs.dev/config/
export default defineConfig(({ command, mode }) => {
  // Load env file based on `mode` in the current working directory.
  // Set the third parameter to '' to load all env regardless of the `VITE_` prefix.
  const env = loadEnv(mode, process.cwd(), '')

  return {
    // Base path for GitHub Pages deployment
    // Change this to your repository name if different from 'Storix'
    // For user/organization pages (username.github.io), set base: '/'
    base: command === 'build' ? '/Storix/' : '/',
    define: {
      'import.meta.env.ACCOUNTS_SCRIPT_ID': JSON.stringify(env.ACCOUNTS_SCRIPT_ID),
    },
    plugins: [
      react(),
      VitePWA({
        registerType: 'autoUpdate',
        includeAssets: ['favicon.ico', 'apple-touch-icon.png', 'masked-icon.svg'],
        manifest: {
          name: 'Storix Inventory',
          short_name: 'Storix',
          description: 'Storix Inventory Management System',
          theme_color: '#ffffff',
          icons: [
            {
              src: 'pwa-192x192.png',
              sizes: '192x192',
              type: 'image/png'
            },
            {
              src: 'pwa-512x512.png',
              sizes: '512x512',
              type: 'image/png'
            }
          ]
        }
      })
    ],
  }
})
